package com.zhangfy.java.basics;

/*
比较运算符，记过都是boolean类型
*/
public class CompareTest {

	public static void main(String[] args) {
		int i = 10;
		int j = 20;
		System.out.println(i == j);
		System.out.println(i = j);

		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1 == b2);
		System.out.println(b2 = b1);
	}

}
